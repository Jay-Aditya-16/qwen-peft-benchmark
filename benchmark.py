import gc
import time
import math
import torch
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt

from datasets import load_dataset
from transformers import (
    AutoTokenizer,
    AutoModelForCausalLM,
    BitsAndBytesConfig,
    TrainingArguments,
    Trainer,
    DataCollatorForLanguageModeling,
)
from peft import (
    LoraConfig,
    get_peft_model,
    prepare_model_for_kbit_training,
    TaskType,
)

print("Torch:", torch.__version__)
print("CUDA:", torch.version.cuda)
print("GPU:", torch.cuda.get_device_name(0))
print("VRAM GB:", round(torch.cuda.get_device_properties(0).total_memory / 1024**3, 2))

MODEL_ID = "Qwen/Qwen2.5-3B-Instruct"

TRAIN_SAMPLES = 300
VAL_SAMPLES = 80

MAX_LEN = 64
MAX_STEPS = 80

RANK = 16
ALPHA = 32
DROPOUT = 0.05
LR = 2e-4

SEED = 42
OUTPUT_ROOT = "/content/peft_benchmark"

torch.manual_seed(SEED)
np.random.seed(SEED)

dataset = load_dataset("yahma/alpaca-cleaned", split="train")
dataset = dataset.shuffle(seed=SEED)

train_ds_raw = dataset.select(range(TRAIN_SAMPLES))
val_ds_raw = dataset.select(range(TRAIN_SAMPLES, TRAIN_SAMPLES + VAL_SAMPLES))

def format_alpaca(example):
    instruction = example["instruction"].strip()
    inp = example["input"].strip()
    output = example["output"].strip()

    if inp:
        text = (
            f"### Instruction:\n{instruction}\n\n"
            f"### Input:\n{inp}\n\n"
            f"### Response:\n{output}"
        )
    else:
        text = (
            f"### Instruction:\n{instruction}\n\n"
            f"### Response:\n{output}"
        )
    return {"text": text}

train_ds = train_ds_raw.map(format_alpaca, remove_columns=train_ds_raw.column_names)
val_ds = val_ds_raw.map(format_alpaca, remove_columns=val_ds_raw.column_names)

print(train_ds[0]["text"][:400])

tokenizer = AutoTokenizer.from_pretrained(MODEL_ID, trust_remote_code=True)
tokenizer.pad_token = tokenizer.eos_token
tokenizer.padding_side = "right"

print("Tokenizer loaded:", len(tokenizer))

RESULTS = []

def clear_mem():
    gc.collect()
    torch.cuda.empty_cache()
    try:
        torch.cuda.ipc_collect()
    except:
        pass
    torch.cuda.reset_peak_memory_stats()

def peak_vram_mb():
    return torch.cuda.max_memory_allocated() / 1024**2

def trainable_params(model):
    trainable = sum(p.numel() for p in model.parameters() if p.requires_grad)
    total = sum(p.numel() for p in model.parameters())
    return trainable, total, 100 * trainable / total

def last_logged_train_loss(log_history):
    for item in reversed(log_history):
        if "loss" in item:
            return float(item["loss"])
    return None

def tokenize_function(example):
    return tokenizer(
        example["text"],
        truncation=True,
        max_length=MAX_LEN,
        padding="max_length"
    )

tokenized_train = train_ds.map(tokenize_function, batched=True, remove_columns=["text"])
tokenized_val = val_ds.map(tokenize_function, batched=True, remove_columns=["text"])

data_collator = DataCollatorForLanguageModeling(
    tokenizer=tokenizer,
    mlm=False
)

print(tokenized_train[0].keys())

def run_experiment(name, quantized=False, use_dora=False):
    clear_mem()

    if quantized:
        bnb_cfg = BitsAndBytesConfig(
            load_in_4bit=True,
            bnb_4bit_quant_type="nf4",
            bnb_4bit_compute_dtype=torch.float16,
            bnb_4bit_use_double_quant=True,
        )

        model = AutoModelForCausalLM.from_pretrained(
            MODEL_ID,
            quantization_config=bnb_cfg,
            device_map="auto",
            trust_remote_code=True,
        )

        model = prepare_model_for_kbit_training(model)

    else:
        model = AutoModelForCausalLM.from_pretrained(
            MODEL_ID,
            torch_dtype=torch.float16,
            device_map="auto",
            low_cpu_mem_usage=True,
            trust_remote_code=True,
        )

    model.config.use_cache = False
    model.gradient_checkpointing_enable()

    try:
        model.enable_input_require_grads()
    except:
        pass

    peft_cfg = LoraConfig(
        r=RANK,
        lora_alpha=ALPHA,
        lora_dropout=DROPOUT,
        bias="none",
        task_type=TaskType.CAUSAL_LM,
        target_modules=[
            "q_proj", "k_proj", "v_proj", "o_proj",
            "gate_proj", "up_proj", "down_proj"
        ],
        use_dora=use_dora,
    )

    model = get_peft_model(model, peft_cfg)

    trainable, total, pct = trainable_params(model)
    print(f"{name}: {trainable:,} / {total:,} trainable ({pct:.4f}%)")

    args = TrainingArguments(
    output_dir=f"{OUTPUT_ROOT}/{name}",

    max_steps=MAX_STEPS,

    learning_rate=LR,

    per_device_train_batch_size=1,
    per_device_eval_batch_size=1,

    gradient_accumulation_steps=8,

    logging_steps=10,

    eval_strategy="steps",

    eval_steps=MAX_STEPS,

    save_strategy="no",

    report_to="none",

    fp16=True,

    dataloader_pin_memory=False,

    seed=SEED,
)

    trainer = Trainer(
        model=model,
        args=args,
        train_dataset=tokenized_train,
        eval_dataset=tokenized_val,
        data_collator=data_collator,
    )

    start = time.time()
    trainer.train()
    elapsed = time.time() - start

    metrics = trainer.evaluate()
    eval_loss = float(metrics["eval_loss"])
    perplexity = float(math.exp(eval_loss))

    RESULTS.append({
        "method": name,
        "eval_loss": eval_loss,
        "perplexity": perplexity,
        "peak_vram_mb": round(peak_vram_mb(), 1),
        "time_s": round(elapsed, 1),
        "trainable_params": trainable,
        "trainable_pct": round(pct, 6),
    })

    del trainer
    del model
    clear_mem()

run_experiment("QLoRA", quantized=True, use_dora=False)

run_experiment("QDoRA", quantized=True, use_dora=True)

run_experiment("LoRA", quantized=False, use_dora=False)

run_experiment("DoRA", quantized=False, use_dora=True)

df = pd.DataFrame(RESULTS)
df = df.sort_values("eval_loss").reset_index(drop=True)
df

for _, row in df.iterrows():
    print(
        f"{row['method']}: "
        f"eval_loss={row['eval_loss']:.4f}, "
        f"ppl={row['perplexity']:.2f}, "
        f"peak_vram={row['peak_vram_mb']:.0f} MB, "
        f"time={row['time_s']:.1f}s"
    )

fig, axes = plt.subplots(1, 2, figsize=(14, 5))

df.plot(x="method", y="eval_loss", kind="bar", ax=axes[0], legend=False)
axes[0].set_title("Validation Loss")
axes[0].set_xlabel("")
axes[0].set_ylabel("loss")

df.plot(x="method", y="peak_vram_mb", kind="bar", ax=axes[1], legend=False)
axes[1].set_title("Peak VRAM")
axes[1].set_xlabel("")
axes[1].set_ylabel("MB")

plt.tight_layout()
plt.show()

df.to_csv("/content/peft_benchmark_results.csv", index=False)
